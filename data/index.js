function mergeRooms(hotel) {
  const hotelName = hotel.name;
  const link = hotel.link;
  const roomTypes = hotel.room_types.map((roomType) => ({
    hotelName,
    link,
    ...roomType,
  }));
  return roomTypes;
}

function getRandomizedRooms(hotels) {
  const allRooms = hotels.flatMap((hotel) => mergeRooms(hotel));
  return allRooms.sort(() => Math.random() - 0.5);
}

function getAllRooms(hotels) {
  const allRooms = hotels.flatMap((hotel) => mergeRooms(hotel));
  return allRooms;
}

